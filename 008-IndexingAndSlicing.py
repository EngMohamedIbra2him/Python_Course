#  Indexing And Slicing

# String Is a Sequence Object in Python 
# Python indexing start with 0 index 

# Indexing (Accessing single Item)
my_string = "I Love Python"

print(my_string[0])
print(my_string[2])
print(my_string[-1])
print(my_string[-6])

# Slicing (Accessing Multiple Items) 
# [start : End]
# [start : End : steps]

print(my_string[2:7])
print(my_string[:7])
print(my_string[2:])


print(my_string[::1])
print(my_string[::2])


