# Arithmetic Operators

# Addition 
print(5+3)
print(5+0.55)

# Subtraction 
print(5-3)
print(5-0.55)
print(-3-2)

# Multiplication 
print(5*3)
print(5*0.55)

# Division 
print(10/2)
print(int(5/0.5))

# Modulus 
print(8%2) #0
print(19%2) #1

# Exponential (Power)
print(2**5) #32
print(12**0) #1