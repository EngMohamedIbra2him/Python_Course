# Built In Functions Part 3

# abs()
print(abs(100))
print(abs(-100))

# pow(base , expon , moduls)
print(pow(2,3))
print(pow(2,5,10))

# min(item1,item2,item3, or iterable Object)
my_list=[1,2,3,4,5,6,7,8,100]
print(min(my_list))

# max(item1,item2,item3, or iterable Object)
print(max(my_list))

# slice(start,end,step)
my_list=[1,2,3,4,5,6,7,8,100]
print(my_list[1:])
print(my_list[slice(1,4)])
