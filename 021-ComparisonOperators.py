# Comparison Operators

# == Equal 
# > greater than
# < less than
# != not equal
# >= greater than or Equal
# <= less than or Equal

# == Equal 
# != not equal
print(100==100)
print(100!=100)

# > greater than
# < less than
print(101>100)
print(101<100)

# >= greater than or Equal
# <= less than or Equal
print(100>=100)
print(100<=100)